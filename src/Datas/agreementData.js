    // 3. 이용자는 운영기관 또는 게시자의 사전 동의가 없이는 서비스를 이용하여 얻은 정보를 복제, 전송, 출판, 배포, 방송 기타 방법에 의하여 영리목적으로 이용하거나 제3자에게 이용하게 하여서는 안 됩니다.
    // 제4장 기타
    // 제14조(면책조항)
    // ① 운영기관은 천재지변 또는 이에 준하는 불가항력으로 인하여 서비스를 제공할 수 없는 경우에는 서비스 제공에 관한 책임이 면제됩니다.
    // ③ 운영기관은 이용자의 귀책사유로 인한 서비스 이용의 장애에 대하여 책임을 지지 않습니다.
    // ② 운영기관은 이용자가 서비스 이용으로 기대하는 이익이나, 발생한 손해에 대하여 책임을 지지 않습니다.
    // ④ 운영기관은 이용자가 서비스에 게재한 정보, 자료, 사실의 신뢰도, 정확성 등의 내용에 관하여는 책임을 지지 않습니다.
    // 제15조(분쟁조정)
    // ①서비스 이용과 관련하여 운영기관과 이용자 사이에 분쟁이 발생한 경우, 운영기관과 이용자는 발생한 분쟁을 원만하게 해결하기 위하여 필요한 모든 노력을 하여야 합니다.
